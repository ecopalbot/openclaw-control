/**
 * ═══════════════════════════════════════════════════════════════
 * Multi-Agent Orchestrator
 *
 * Runs a pipeline of agents sequentially, passing each agent's
 * output as context into the next. Every agent uses the real
 * `openclaw agent --agent <name> --message ...` CLI natively.
 *
 * Usage:
 *   node multi-agent-orchestrator.js
 * ═══════════════════════════════════════════════════════════════
 */

require('dotenv').config({ path: require('path').resolve(__dirname, '../.env') });
const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const OPENCLAW = '/opt/homebrew/bin/openclaw';
const PROJECTS_DIR = path.join(process.env.HOME, 'Documents', 'Projects', 'ecopalbot-projects', 'financial-dashboard-e2e');

// ── Helper: run one agent turn and return its output ─────────────
function runAgent(agentName, message) {
  console.log(`\n${'─'.repeat(60)}`);
  console.log(`🤖 [${agentName.toUpperCase()}] Running...`);
  console.log(`   Task: ${message.slice(0, 120)}`);

  // Escape single quotes
  const safe = message.replace(/'/g, "'\\''");
  const cmd = `${OPENCLAW} agent --agent ${agentName} --local --message '${safe}'`;

  try {
    const output = execSync(cmd, {
      encoding: 'utf8',
      timeout: 240000, // 4 min per agent
      maxBuffer: 1024 * 1024 * 5,
      env: { ...process.env, OPENCLAW_IGNORE_CONFIG_ERRORS: '1' }
    }).trim();

    // Strip ANSI and OpenClaw boilerplate
    const clean = output
      .replace(/[\u001b\u009b][[()#;?]*(?:[0-9]{1,4}(?:;[0-9]{0,4})*)?[0-9A-ORZcf-nqry=><]/g, '')
      .split('\n')
      .filter(l => {
        const t = l.trim();
        return t.length > 0
          && !t.startsWith('│')
          && !t.startsWith('◇')
          && !t.startsWith('🦞')
          && !t.startsWith('Config');
      })
      .join('\n')
      .trim();

    console.log(`✅ [${agentName.toUpperCase()}] Done (${clean.length} chars)`);
    return clean;
  } catch (err) {
    const errOut = (err.stdout || err.message || '').trim();
    console.error(`❌ [${agentName.toUpperCase()}] Failed: ${errOut.slice(0, 200)}`);
    return `[${agentName} AGENT ERROR]: ${errOut.slice(0, 500)}`;
  }
}

// ── Save report to disk ──────────────────────────────────────────
function saveReport(sections) {
  fs.mkdirSync(PROJECTS_DIR, { recursive: true });
  const reportPath = path.join(PROJECTS_DIR, 'REPORT.md');
  const date = new Date().toISOString().replace('T', ' ').slice(0, 19);

  const content = `# 💰 Financial App Dashboard — Multi-Agent E2E Report
> Generated: ${date} | Agents: anna, architect, builder, reviewer

---

## 1️⃣ UI/UX Design — *Anna (UX Designer)*
${sections.anna}

---

## 2️⃣ Architecture & Data Structure — *Architect*
${sections.architect}

---

## 3️⃣ Frontend Implementation — *Builder*
${sections.builder}

---

## 4️⃣ Review & QA — *Reviewer*
${sections.reviewer}

---
*This report was autonomously generated by the OpenClaw Multi-Agent Orchestrator.*
`;

  fs.writeFileSync(reportPath, content);
  console.log(`\n📄 Report saved to: ${reportPath}`);
  return reportPath;
}

// ── Main Pipeline ────────────────────────────────────────────────
async function run() {
  console.log('🚀 Starting Multi-Agent Financial Dashboard Pipeline\n');

  // Stage 1: Anna defines UX
  const annaOut = runAgent('anna',
    'You are a UX designer. Define the UI/UX requirements for a comprehensive financial app dashboard. ' +
    'Include: layout sections (sidebar, charts, KPIs), colour palette, typography, and key user workflows. ' +
    'Output in clean Markdown with headers.'
  );

  // Stage 2: Architect designs data model, receives anna's context
  const architectOut = runAgent('architect',
    `You are a software architect. Based on the following UI/UX specification, design the data structure for a financial dashboard backend:\n\n${annaOut}\n\n` +
    'Define: database tables (PostgreSQL), API endpoints (REST), and a brief explanation of each model. Output in clean Markdown.'
  );

  // Stage 3: Builder implements frontend, receives both prior outputs
  const builderOut = runAgent('builder',
    `You are a frontend developer. Based on the following UI/UX spec and data architecture, write a single self-contained HTML file for a financial dashboard with inline CSS and JavaScript.\n\n` +
    `UI/UX Spec:\n${annaOut}\n\nData model summary:\n${architectOut}\n\n` +
    'Output ONLY the complete HTML/CSS/JS file content. Use a dark theme. Keep it under 200 lines.'
  );

  // Stage 4: Reviewer audits the builder output
  const reviewerOut = runAgent('reviewer',
    `You are a code reviewer. Review the following HTML frontend implementation and provide a quality assessment:\n\n${builderOut}\n\n` +
    'Check for: accessibility, responsive design, code quality, and completeness. Output as Markdown with a score out of 10.'
  );

  // Compile report
  const reportPath = saveReport({
    anna: annaOut,
    architect: architectOut,
    builder: builderOut,
    reviewer: reviewerOut
  });

  // Also save the generated HTML
  const htmlMatch = builderOut.match(/```html([\s\S]*?)```/) || builderOut.match(/<!DOCTYPE html>([\s\S]*)/i);
  if (htmlMatch) {
    const htmlPath = path.join(PROJECTS_DIR, 'dashboard.html');
    fs.writeFileSync(htmlPath, htmlMatch[1] || htmlMatch[0]);
    console.log(`🌐 HTML saved to: ${htmlPath}`);
  }

  console.log('\n✅ Multi-Agent Pipeline Complete!');
  console.log(`📁 Project folder: ${PROJECTS_DIR}`);
  return reportPath;
}

run().catch(err => {
  console.error('Fatal orchestrator error:', err);
  process.exit(1);
});
